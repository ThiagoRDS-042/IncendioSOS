    public void relatorio(){
        
        DateTimeFormatter formatoData = DateTimeFormatter.ofPattern("MM");
        LocalDateTime data = LocalDateTime.now();
      
        List<Notificacao> nots = (List<Notificacao>) dao.relatorio(formatoData.format(data), "Notificação");
        List<Denuncia> dens = (List<Denuncia>) dao.relatorio(formatoData.format(data), "Denuncia");
        
        int qtd = nots.size();
        qtd += dens.size();
        
        jlQtd.setText(Integer.toString(qtd));
    }

